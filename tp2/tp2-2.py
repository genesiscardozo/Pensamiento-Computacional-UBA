#Desarrollar un programa que permita ingresar dos números enteros A y B a través del teclado.
#Imprimir su suma y su diferencia.

numA = int(input("Ingrese un número: "))
numB = int(input("Ingrese otro número: "))
sumaAB = numA + numB
restaAB = numA - numB

print("La suma de los dos números es " + str(sumaAB) + " La resta de los dos números es " + str(restaAB))

